    private void tblDSPhieuGiaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDSPhieuGiaoMouseClicked
        // TODO add your handling code here:
        List<TTGiaoHang> danhSachPhieuGiao = gh.getPhieuGiao();
        int vt = tblDSPhieuGiao.getSelectedRow();

        if (vt != -1) {
            TTGiaoHang phieuGiao = danhSachPhieuGiao.get(vt);

            if (phieuGiao != null) {

                txtDiaChi.setText(phieuGiao.getDiaChiGiaoHang());
                txtNgayGiao.setText(phieuGiao.getNgayGiaoHang());
                txtPhuongThuc.setText(phieuGiao.getPhuongThucGiaoHang());
                txtTrangThai.setText(phieuGiao.getTrangThai());
                txtMaPG.setText(phieuGiao.getMaGH());
            }
        }
    }//GEN-LAST:event_tblDSPhieuGiaoMouseClicked

