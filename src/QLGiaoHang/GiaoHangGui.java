    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        List<TTGiaoHang> danhSachPhieuGiao = gh.timPhieuGiao(txtTimKiem.getText());
    String[] header = {"MaGH", "DiaChiGiaoHang", "NgayGiaoHang", "PhuongThucGiaoHang", "TrangThai"};
    DefaultTableModel model = new DefaultTableModel(header, 0);
    
    for (TTGiaoHang pg : danhSachPhieuGiao) {
        model.addRow(new Object[]{
            pg.getMaGH(),
            pg.getDiaChiGiaoHang(),
            pg.getNgayGiaoHang(),
            pg.getPhuongThucGiaoHang(),
            pg.getTrangThai()
        });
    }    
    tblDSPhieuGiao.setModel(model);
    }//GEN-LAST:event_btnTimKiemActionPerformed

