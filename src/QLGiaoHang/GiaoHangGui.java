    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
        // Kiểm tra xem trường mã phiếu giao có trống không
        if (txtMaPG.getText().isEmpty()) {
            JOptionPane.showMessageDialog(btnXoa, "Thiếu thông tin mã phiếu giao");
        } else {
            int response = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa phiếu giao?", 
                                                         "Xác nhận xóa", JOptionPane.YES_NO_OPTION, 
                                                         JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.YES_OPTION) {
                boolean success = gh.xoaGiaoHang(txtMaPG.getText()); 
                if (success) {
                    JOptionPane.showMessageDialog(btnXoa, "Đã xóa phiếu giao");
                    showPhieuGiao(); 
                    reset();
                } else {
                    JOptionPane.showMessageDialog(btnXoa, "Lỗi khi xóa phiếu giao");
                }
            }
        }
    } catch (Exception e) {
        e.printStackTrace(); 
        JOptionPane.showMessageDialog(btnXoa, "Đã xảy ra lỗi: " + e.getMessage());
    }
    }//GEN-LAST:event_btnXoaActionPerformed
    public void updatePhieuGiao() {
        try {
            if (txtMaPG.getText().isEmpty() || txtDiaChi.getText().isEmpty() ||
                txtNgayGiao.getText().isEmpty() || txtPhuongThuc.getText().isEmpty() ||
                txtTrangThai.getText().isEmpty()) {
                JOptionPane.showMessageDialog(btnSua, "Thiếu thông tin");
            } else {
                TTGiaoHang phieuGiao = new TTGiaoHang(
                    txtMaPG.getText(),
                    txtDiaChi.getText(),
                    txtNgayGiao.getText(),
                    txtPhuongThuc.getText(),
                    txtTrangThai.getText()
                );
                boolean isUpdated = gh.suaGiaoHang(phieuGiao);
                if (isUpdated) {
                    JOptionPane.showMessageDialog(btnSua, "Cập nhật thành công");
                } else {
                    JOptionPane.showMessageDialog(btnSua, "Cập nhật thất bại");
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(btnSua, "Đã xảy ra lỗi: " + e.getMessage());
        }
        showPhieuGiao(); 
}

