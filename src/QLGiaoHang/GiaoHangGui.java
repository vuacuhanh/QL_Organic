    public void showPhieuGiao() {
    List<TTGiaoHang> danhSachPhieuGiao = gh.getPhieuGiao(); 
    String[] header = {"Mã GH", "Địa Chỉ Giao", "Ngày Giao", "Phương Thức", "Trạng Thái"}; 
    DefaultTableModel model = new DefaultTableModel(header, 0); 
    for (TTGiaoHang phieuGiao : danhSachPhieuGiao) {
        model.addRow(new Object[]{
            phieuGiao.getMaGH(),
            phieuGiao.getDiaChiGiaoHang(),
            phieuGiao.getNgayGiaoHang(),
            phieuGiao.getPhuongThucGiaoHang(),
            phieuGiao.getTrangThai()
        });
    }

    tblDSPhieuGiao.setModel(model); 
}

